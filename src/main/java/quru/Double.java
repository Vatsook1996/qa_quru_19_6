package quru;

public class Double {
    public static void main(String[] args) {


        int numberInt = 80;
        double numberdouble = 12.142;

        System.out.println("numberInt + numberdouble =" + (numberInt + numberdouble));
        System.out.println("numberInt * numberdouble =" + (numberInt * numberdouble));
        System.out.println("numberInt / numberdouble =" + (numberInt / numberdouble));
    }
}

